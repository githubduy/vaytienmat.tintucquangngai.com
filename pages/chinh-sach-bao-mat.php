<h2 class="section-title">Chính Sách Bảo Mật</h2>
<p>VayNhanhQN cam kết bảo vệ quyền riêng tư và thông tin cá nhân của khách hàng.</p>

<h3>1. Thu thập thông tin</h3>
<p>Chúng tôi chỉ thu thập các thông tin cần thiết để phục vụ quá trình tư vấn và làm hồ sơ vay vốn, bao gồm: Họ tên, số điện thoại, địa chỉ, số CMND/CCCD...</p>

<h3>2. Sử dụng thông tin</h3>
<p>Thông tin của bạn được sử dụng để:</p>
<ul>
    <li>Liên hệ tư vấn khoản vay.</li>
    <li>Thẩm định hồ sơ vay vốn tại các đối tác tài chính (SHB Finance, FE Credit...).</li>
    <li>Thông báo về các chương trình khuyến mãi, ưu đãi.</li>
</ul>

<h3>3. Chia sẻ thông tin</h3>
<p>Chúng tôi cam kết không bán, trao đổi thông tin khách hàng cho bên thứ ba không liên quan. Thông tin chỉ được chia sẻ với các đối tác tài chính trực tiếp xử lý hồ sơ vay của bạn.</p>